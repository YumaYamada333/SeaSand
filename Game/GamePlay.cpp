//----------------------------------------------------------------------
//! @brief ゲームプレイ処理
//!
//! @param[in] なし
//!
//! @return なし
//----------------------------------------------------------------------
void Play::Update()
{
	//シーンの最初に一回だけ行う初期化処理
	if (g_init == 0)
	{
		g_init = 1;
	}	

	if (g_mouse.leftButton)
	{
		g_NextScene = CLEAR;
	}
}

//----------------------------------------------------------------------
//! @brief ゲームプレイ描画処理
