//! @return なし
//----------------------------------------------------------------------
void Play::Render()
{
	wchar_t buf[256];

	swprintf_s(buf, 256, L"PLAY");
	g_spriteFont->DrawString(g_spriteBatch.get(), buf, Vector2(100, 0));
}
